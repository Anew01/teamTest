package kr.or.iei.frip.vo;

public class FripMainDate {
	private int totalIncome;
	private int totalInquiryCount;
	private double totalrankCount;
	private int totalFeedCount;
	private int totalPaymentCount;
	private int totalFripCount;
	private int totalExpiredFripCount;
	public int getTotalIncome() {
		return totalIncome;
	}
	public void setTotalIncome(int totalIncome) {
		this.totalIncome = totalIncome;
	}
	public int getTotalInquiryCount() {
		return totalInquiryCount;
	}
	public void setTotalInquiryCount(int totalInquiryCount) {
		this.totalInquiryCount = totalInquiryCount;
	}
	public double getTotalrankCount() {
		return totalrankCount;
	}
	public void setTotalrankCount(double totalrankCount) {
		this.totalrankCount = totalrankCount;
	}
	public int getTotalFeedCount() {
		return totalFeedCount;
	}
	public void setTotalFeedCount(int totalFeedCount) {
		this.totalFeedCount = totalFeedCount;
	}
	public int getTotalPaymentCount() {
		return totalPaymentCount;
	}
	public void setTotalPaymentCount(int totalPaymentCount) {
		this.totalPaymentCount = totalPaymentCount;
	}
	public int getTotalFripCount() {
		return totalFripCount;
	}
	public void setTotalFripCount(int totalFripCount) {
		this.totalFripCount = totalFripCount;
	}
	public int getTotalExpiredFripCount() {
		return totalExpiredFripCount;
	}
	public void setTotalExpiredFripCount(int totalExpiredFripCount) {
		this.totalExpiredFripCount = totalExpiredFripCount;
	}
}
